// knownUnsignedCases is the set of well-known unsigned magic divisor constants
// generated by gcc/clang -O2 for 32-bit unsigned division.
// each entry: (divisor, magic, shift).
// these are mathematically verified: M = ceil(2^(32+S) / D).
var knownUnsignedCases = []struct {
	d     uint64
	magic uint64
	shift uint8
}{
	{3, 0xAAAAAAAB, 1},
	{5, 0x66666667, 1},
	{6, 0xAAAAAAAB, 2},
	{7, 0x92492493, 2},
	{9, 0x38E38E39, 1},
	{10, 0xCCCCCCCD, 3},
	{11, 0xBA2E8BA3, 3},
	{12, 0xAAAAAAAB, 3},
	{13, 0x4EC4EC4F, 2},
	{14, 0x92492493, 3},
	{15, 0x88888889, 3},
	{17, 0x78787879, 3},
	{25, 0x51EB851F, 3},
	{100, 0x51EB851F, 5},
}

// knownSignedCases is the set of well-known signed magic divisor constants.
// each entry: (divisor, magic, shift).
// these use arithmetic right shift (SAR) and the Hacker's Delight formula.
// only constants verified to pass verifySigned are included here.
var knownSignedCases = []struct {
	d     uint64
	magic uint64
	shift uint8
}{
	{7, 0x92492493, 2},
	{9, 0x38E38E39, 1},
	{11, 0x2E8BA2E9, 1},
	{13, 0x4EC4EC4F, 2},
}

// genUnsignedMagicDivCase generates a random unsigned magic divisor test case
// from the set of known compiler-generated constants.
func genUnsignedMagicDivCase() gopter.Gen {
	return func(params *gopter.GenParameters) *gopter.GenResult {
		idx := params.NextUint64() % uint64(len(knownUnsignedCases))
		c := knownUnsignedCases[idx]
		tc := &magicDivTestCase{
			divisor: c.d,
			magic:   c.magic,
			shift:   c.shift,
			kind:    MagicDivUnsigned,
		}
		return gopter.NewGenResult(tc, gopter.NoShrinker)
	}
}

// genSignedMagicDivCase generates a random signed magic divisor test case
// from the set of known compiler-generated constants.
func genSignedMagicDivCase() gopter.Gen {
	return func(params *gopter.GenParameters) *gopter.GenResult {
		idx := params.NextUint64() % uint64(len(knownSignedCases))
		c := knownSignedCases[idx]
		tc := &magicDivTestCase{
			divisor: c.d,
			magic:   c.magic,
			shift:   c.shift,
			kind:    MagicDivSigned,
		}
		return gopter.NewGenResult(tc, gopter.NoShrinker)
	}
}

// genNonMagicCase generates an IR block that does NOT contain a magic divisor pattern.
// uses arithmetic operations that are structurally different from mul+shift.
func genNonMagicCase() gopter.Gen {
	return func(params *gopter.GenParameters) *gopter.GenResult {
		// non-magic ops: add, sub, and, or, xor (not mul followed by shift)
		nonMagicOps := []ir.BinaryOperator{
			ir.BinOpAdd, ir.BinOpSub, ir.BinOpAnd, ir.BinOpOr, ir.BinOpXor,
		}
		op := nonMagicOps[params.NextUint64()%uint64(len(nonMagicOps))]
		constVal := params.NextUint64()%1000 + 1

		tc := &nonMagicCase{
			op:       op,
			constVal: constVal,
		}
		return gopter.NewGenResult(tc, gopter.NoShrinker)
	}
}

// genMultiMagicCase generates a block with 2-4 magic divisor patterns.
// uses distinct dividend variables and result variables to avoid conflicts.
func genMultiMagicCase() gopter.Gen {
	return func(params *gopter.GenParameters) *gopter.GenResult {
		// pick 2-4 patterns from the known unsigned cases
		numPatterns := int(params.NextUint64()%3) + 2
		if numPatterns > len(knownUnsignedCases) {
			numPatterns = len(knownUnsignedCases)
		}

		// select distinct cases by shuffling indices
		indices := make([]int, len(knownUnsignedCases))
		for i := range indices {
			indices[i] = i
		}
		// fisher-yates shuffle using gopter rng
		for i := len(indices) - 1; i > 0; i-- {
			j := int(params.NextUint64() % uint64(i+1))
			indices[i], indices[j] = indices[j], indices[i]
		}

		cases := make([]*magicDivTestCase, numPatterns)
		for i := 0; i < numPatterns; i++ {
			c := knownUnsignedCases[indices[i]]
			cases[i] = &magicDivTestCase{
				divisor: c.d,
				magic:   c.magic,
				shift:   c.shift,
				kind:    MagicDivUnsigned,
			}
		}

		return gopter.NewGenResult(&multiMagicCase{cases: cases}, gopter.NoShrinker)
	}
}

