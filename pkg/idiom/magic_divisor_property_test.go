// checkDivisorRecovery verifies that RecognizeMagicDivisors recovers the correct divisor.
//
// property: for all (D, M, S): recognize(build_block(D, M, S)).Divisor == D
func checkDivisorRecovery(t *testing.T, tc *magicDivTestCase) bool {
	t.Helper()

	block := buildMagicBlockFromCase(tc)
	matches, err := RecognizeMagicDivisors(block)
	if err != nil {
		t.Logf("RecognizeMagicDivisors error: %v (case: %s)", err, describeMagicCase(tc))
		return false
	}

	if len(matches) != 1 {
		t.Logf("expected 1 match, got %d (case: %s)", len(matches), describeMagicCase(tc))
		return false
	}

	if matches[0].Divisor != tc.divisor {
		t.Logf("divisor mismatch: expected %d, got %d (case: %s)",
			tc.divisor, matches[0].Divisor, describeMagicCase(tc))
		return false
	}

	return true
}

// checkBlockRewrite verifies that the block is correctly rewritten after recognition.
//
// property: after recognition, block has exactly 1 instruction (division),
// the multiply instruction is removed, and the division uses the correct divisor.
func checkBlockRewrite(t *testing.T, tc *magicDivTestCase) bool {
	t.Helper()

	block := buildMagicBlockFromCase(tc)
	originalLen := len(block.Instructions) // should be 2: mul + shift

	matches, err := RecognizeMagicDivisors(block)
	if err != nil {
		t.Logf("RecognizeMagicDivisors error: %v (case: %s)", err, describeMagicCase(tc))
		return false
	}

	if len(matches) != 1 {
		t.Logf("expected 1 match, got %d (case: %s)", len(matches), describeMagicCase(tc))
		return false
	}

	// block must shrink by 1 (multiply removed)
	if len(block.Instructions) != originalLen-1 {
		t.Logf("expected %d instructions after rewrite, got %d (case: %s)",
			originalLen-1, len(block.Instructions), describeMagicCase(tc))
		return false
	}

	// the single remaining instruction must be an assignment
	assign, ok := block.Instructions[0].(*ir.Assign)
	if !ok {
		t.Logf("expected *ir.Assign after rewrite, got %T (case: %s)",
			block.Instructions[0], describeMagicCase(tc))
		return false
	}

	// source must be a binary operation
	binop, ok := assign.Source.(*ir.BinaryOp)
	if !ok {
		t.Logf("expected *ir.BinaryOp source, got %T (case: %s)",
			assign.Source, describeMagicCase(tc))
		return false
	}

	// unsigned → BinOpUDiv, signed → BinOpDiv
	expectedOp := ir.BinOpUDiv
	if tc.kind == MagicDivSigned {
		expectedOp = ir.BinOpDiv
	}
	if binop.Op != expectedOp {
		t.Logf("expected op %v, got %v (case: %s)", expectedOp, binop.Op, describeMagicCase(tc))
		return false
	}

	// divisor constant must match
	ce, ok := binop.Right.(*ir.ConstantExpr)
	if !ok {
		t.Logf("expected *ir.ConstantExpr divisor, got %T (case: %s)",
			binop.Right, describeMagicCase(tc))
		return false
	}
	ic, ok := ce.Value.(ir.IntConstant)
	if !ok {
		t.Logf("expected ir.IntConstant, got %T (case: %s)", ce.Value, describeMagicCase(tc))
		return false
	}
	//nolint:gosec // divisor is bounded to [2, 2^31] by recoverDivisor
	if uint64(ic.Value) != tc.divisor {
		t.Logf("divisor constant mismatch: expected %d, got %d (case: %s)",
			tc.divisor, ic.Value, describeMagicCase(tc))
		return false
	}

	return true
}

// checkSemanticEquivalenceMagic verifies that the magic divisor formula is
// semantically equivalent to actual division for a representative sample of values.
//
// property: for all x in sample_set: x / D == (x * M) >> S
//
// this validates requirement 13.5: semantic equivalence after rewrite.
func checkSemanticEquivalenceMagic(t *testing.T, tc *magicDivTestCase) bool {
	t.Helper()

	// sample values covering: 0, 1, D-1, D, D+1, 2D, large values, boundary values
	sampleValues := []uint64{
		0, 1, 2, 3,
		tc.divisor - 1, tc.divisor, tc.divisor + 1,
		2*tc.divisor - 1, 2 * tc.divisor, 2*tc.divisor + 1,
		100, 1000, 10000,
		0xFFFF, 0xFFFFFF, 0xFFFFFFFF,
	}

	for _, x := range sampleValues {
		x32 := x & 0xFFFFFFFF
		expectedDiv := x32 / tc.divisor

		// form 1: (x * M) >> (32 + S)
		got1 := (x32 * tc.magic) >> (32 + uint(tc.shift))

		// form 2: ((x * M) >> 32 + x) >> S  (correction variant for M with bit 32 set)
		hiMul := (x32 * tc.magic) >> 32
		got2 := (hiMul + x32) >> uint(tc.shift)

		if got1 != expectedDiv && got2 != expectedDiv {
			t.Logf("semantic equivalence violated: x=%d D=%d M=0x%x S=%d\n  expected x/D=%d, got form1=%d form2=%d",
				x32, tc.divisor, tc.magic, tc.shift, expectedDiv, got1, got2)
			return false
		}
	}

	return true
}

// checkSignedRewriteOp verifies that signed magic divisor patterns are rewritten
// with BinOpDiv (signed division), not BinOpUDiv (unsigned division).
//
// property: kind == MagicDivSigned → rewritten op == BinOpDiv
func checkSignedRewriteOp(t *testing.T, tc *magicDivTestCase) bool {
	t.Helper()

	if tc.kind != MagicDivSigned {
		// only applies to signed cases
		return true
	}

	block := buildMagicBlockFromCase(tc)
	matches, err := RecognizeMagicDivisors(block)
	if err != nil {
		t.Logf("RecognizeMagicDivisors error: %v (case: %s)", err, describeMagicCase(tc))
		return false
	}

	if len(matches) != 1 {
		t.Logf("expected 1 match, got %d (case: %s)", len(matches), describeMagicCase(tc))
		return false
	}

	if matches[0].Kind != MagicDivSigned {
		t.Logf("expected MagicDivSigned kind, got %v (case: %s)", matches[0].Kind, describeMagicCase(tc))
		return false
	}

	// verify the rewritten instruction uses BinOpDiv
	assign, ok := block.Instructions[0].(*ir.Assign)
	if !ok {
		t.Logf("expected *ir.Assign, got %T (case: %s)", block.Instructions[0], describeMagicCase(tc))
		return false
	}
	binop, ok := assign.Source.(*ir.BinaryOp)
	if !ok {
		t.Logf("expected *ir.BinaryOp, got %T (case: %s)", assign.Source, describeMagicCase(tc))
		return false
	}
	if binop.Op != ir.BinOpDiv {
		t.Logf("expected BinOpDiv for signed division, got %v (case: %s)", binop.Op, describeMagicCase(tc))
		return false
	}

	return true
}

// checkNoFalsePositive verifies that non-magic patterns produce no matches.
//
// property: for all non-magic IR blocks: RecognizeMagicDivisors returns 0 matches
func checkNoFalsePositive(t *testing.T, tc *nonMagicCase) bool {
	t.Helper()

	x := u32Var("x", 1)
	y := u32Var("y", 1)

	// build a block with a single non-magic arithmetic operation
	block := &ir.BasicBlock{
		ID: 0,
		Instructions: []ir.IRInstruction{
			&ir.Assign{
				Dest: y,
				Source: &ir.BinaryOp{
					Op:    tc.op,
					Left:  varExpr(x),
					Right: u32Const(tc.constVal),
				},
			},
		},
	}

	matches, err := RecognizeMagicDivisors(block)
	if err != nil {
		t.Logf("RecognizeMagicDivisors error: %v (op=%v constVal=%d)", err, tc.op, tc.constVal)
		return false
	}

	if len(matches) != 0 {
		t.Logf("false positive: expected 0 matches for non-magic op %v constVal=%d, got %d",
			tc.op, tc.constVal, len(matches))
		return false
	}

	// block must be unmodified
	if len(block.Instructions) != 1 {
		t.Logf("block was modified despite no match: expected 1 instruction, got %d", len(block.Instructions))
		return false
	}

	return true
}

// checkMultiPatternRecognition verifies that all magic divisor patterns in a block
// are recognized and the block is correctly rewritten.
//
// property: for a block with N magic patterns:
//   - RecognizeMagicDivisors returns exactly N matches
//   - block is rewritten to N division instructions (2N → N)
//   - each match has the correct divisor
func checkMultiPatternRecognition(t *testing.T, tc *multiMagicCase) bool {
	t.Helper()

	block := buildMultiMagicBlock(tc.cases)
	originalLen := len(block.Instructions) // 2 * len(cases)
	expectedMatches := len(tc.cases)

	matches, err := RecognizeMagicDivisors(block)
	if err != nil {
		t.Logf("RecognizeMagicDivisors error: %v", err)
		return false
	}

	if len(matches) != expectedMatches {
		t.Logf("expected %d matches, got %d (cases: %s)",
			expectedMatches, len(matches), describeMultiCase(tc))
		return false
	}

	// block must shrink by N (N multiplies removed)
	expectedLen := originalLen - expectedMatches
	if len(block.Instructions) != expectedLen {
		t.Logf("expected %d instructions after rewrite, got %d (cases: %s)",
			expectedLen, len(block.Instructions), describeMultiCase(tc))
		return false
	}

	// all remaining instructions must be division operations
	for i, instr := range block.Instructions {
		assign, ok := instr.(*ir.Assign)
		if !ok {
			t.Logf("instruction %d: expected *ir.Assign, got %T", i, instr)
			return false
		}
		binop, ok := assign.Source.(*ir.BinaryOp)
		if !ok {
			t.Logf("instruction %d: expected *ir.BinaryOp source, got %T", i, assign.Source)
			return false
		}
		if binop.Op != ir.BinOpUDiv && binop.Op != ir.BinOpDiv {
			t.Logf("instruction %d: expected division op, got %v", i, binop.Op)
			return false
		}
	}

	// build a set of expected divisors for cross-checking
	expectedDivisors := make(map[uint64]int)
	for _, c := range tc.cases {
		expectedDivisors[c.divisor]++
	}
	recoveredDivisors := make(map[uint64]int)
	for _, m := range matches {
		recoveredDivisors[m.Divisor]++
	}
	for d, count := range expectedDivisors {
		if recoveredDivisors[d] != count {
			t.Logf("divisor %d: expected %d occurrences, got %d (cases: %s)",
				d, count, recoveredDivisors[d], describeMultiCase(tc))
			return false
		}
	}

	return true
}

