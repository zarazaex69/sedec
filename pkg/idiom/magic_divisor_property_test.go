// IR block builders
// ============================================================================

// buildMagicBlockFromCase constructs a two-instruction basic block encoding
// the magic divisor pattern for the given test case.
//
// structure:
//
//	t1 = dividend * magic
//	result = t1 >> shift  (BinOpShr for unsigned, BinOpSar for signed)
func buildMagicBlockFromCase(tc *magicDivTestCase) *ir.BasicBlock {
	var dividend ir.Variable
	if tc.kind == MagicDivSigned {
		dividend = i32Var("x", 1)
	} else {
		dividend = u32Var("x", 1)
	}

	shiftOp := ir.BinOpShr
	if tc.kind == MagicDivSigned {
		shiftOp = ir.BinOpSar
	}

	return buildMagicBlock(dividend, tc.magic, tc.shift, shiftOp)
}

// buildMultiMagicBlock constructs a basic block with multiple magic divisor patterns.
// each pattern uses a distinct dividend variable (x_i) and result variable (r_i)
// to avoid variable name collisions in the def-index.
func buildMultiMagicBlock(cases []*magicDivTestCase) *ir.BasicBlock {
	instrs := make([]ir.IRInstruction, 0, len(cases)*2)

	for i, tc := range cases {
		// use distinct variable names per pattern to avoid def-index collisions
		dividend := u32Var(fmt.Sprintf("x%d", i), 1)
		t1 := u32Var(fmt.Sprintf("t%d", i), 1)
		result := u32Var(fmt.Sprintf("r%d", i), 1)

		shiftOp := ir.BinOpShr
		if tc.kind == MagicDivSigned {
			shiftOp = ir.BinOpSar
		}

		// mul instruction: t_i = x_i * magic
		instrs = append(instrs, &ir.Assign{
			Dest: t1,
			Source: &ir.BinaryOp{
				Op:    ir.BinOpMul,
				Left:  varExpr(dividend),
				Right: u32Const(tc.magic),
			},
		})

		// shift instruction: r_i = t_i >> shift
		instrs = append(instrs, &ir.Assign{
			Dest: result,
			Source: &ir.BinaryOp{
				Op:    shiftOp,
				Left:  varExpr(t1),
				Right: u32Const(uint64(tc.shift)),
			},
		})
	}

	return &ir.BasicBlock{
		ID:           0,
		Instructions: instrs,
	}
}

