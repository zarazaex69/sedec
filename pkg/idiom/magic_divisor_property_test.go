// describeMagicCase returns a human-readable description of a magicDivTestCase.
func describeMagicCase(tc *magicDivTestCase) string {
	kindStr := "unsigned"
	if tc.kind == MagicDivSigned {
		kindStr = "signed"
	}
	return fmt.Sprintf("%s D=%d M=0x%x S=%d", kindStr, tc.divisor, tc.magic, tc.shift)
}

// describeMultiCase returns a human-readable description of a multiMagicCase.
func describeMultiCase(tc *multiMagicCase) string {
	s := fmt.Sprintf("%d patterns: ", len(tc.cases))
	for i, c := range tc.cases {
		if i > 0 {
			s += ", "
		}
		s += fmt.Sprintf("D=%d", c.divisor)
	}
	return s
}
