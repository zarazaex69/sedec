package idiom

// TestProperty19: Magic Divisor Recognition Correctness
//
// property 19: for all valid magic divisor patterns (M, S, D):
//
//  1. recognition: RecognizeMagicDivisors correctly identifies the pattern
//  2. correctness: recovered divisor D' == original divisor D
//  3. rewrite:     block is rewritten to a single division instruction
//  4. semantic:    for all x in sample set: x / D == (x * M) >> S
//
// validates requirements 13.1-13.6:
//   - 13.1: recognize magic divisor patterns in IR
//   - 13.2: recover original divisor from (M, S) pair
//   - 13.3: rewrite multiply+shift to explicit division
//   - 13.4: handle both signed and unsigned variants
//   - 13.5: preserve semantic equivalence after rewrite
//   - 13.6: handle multiple patterns in the same block
//
// minimum 100 iterations required.

import (
	"fmt"
	"testing"

	"github.com/leanovate/gopter"
	"github.com/leanovate/gopter/prop"
	"github.com/zarazaex69/sedec/pkg/ir"
)

// ============================================================================
// property test entry point
// ============================================================================

// TestProperty19 verifies magic divisor recognition correctness.
// runs 100+ iterations with gopter-generated magic divisor patterns.
func TestProperty19(t *testing.T) {
	params := gopter.DefaultTestParameters()
	params.MinSuccessfulTests = 100
	params.MaxSize = 8

	properties := gopter.NewProperties(params)

	// property 19a: recognized divisor matches the original divisor
	properties.Property("recovered divisor equals original divisor (unsigned)", prop.ForAll(
		func(tc *magicDivTestCase) bool {
			return checkDivisorRecovery(t, tc)
		},
		genUnsignedMagicDivCase(),
	))

	// property 19b: block is correctly rewritten to a single division instruction
	properties.Property("block rewritten to single division instruction (unsigned)", prop.ForAll(
		func(tc *magicDivTestCase) bool {
			return checkBlockRewrite(t, tc)
		},
		genUnsignedMagicDivCase(),
	))

	// property 19c: semantic equivalence — x/D == (x*M)>>S for sample values
	properties.Property("semantic equivalence: x/D == (x*M)>>S for sample values", prop.ForAll(
		func(tc *magicDivTestCase) bool {
			return checkSemanticEquivalenceMagic(t, tc)
		},
		genUnsignedMagicDivCase(),
	))

	// property 19d: signed magic divisor recovery is correct
	properties.Property("recovered divisor equals original divisor (signed)", prop.ForAll(
		func(tc *magicDivTestCase) bool {
			return checkDivisorRecovery(t, tc)
		},
		genSignedMagicDivCase(),
	))

	// property 19e: signed block rewrite uses BinOpDiv (not BinOpUDiv)
	properties.Property("signed block rewritten with BinOpDiv", prop.ForAll(
		func(tc *magicDivTestCase) bool {
			return checkSignedRewriteOp(t, tc)
		},
		genSignedMagicDivCase(),
	))

	// property 19f: non-magic patterns are not falsely recognized
	properties.Property("non-magic patterns produce no matches", prop.ForAll(
		func(tc *nonMagicCase) bool {
			return checkNoFalsePositive(t, tc)
		},
		genNonMagicCase(),
	))

	// property 19g: multiple patterns in one block are all recognized
	properties.Property("multiple magic patterns in one block are all recognized", prop.ForAll(
		func(tc *multiMagicCase) bool {
			return checkMultiPatternRecognition(t, tc)
		},
		genMultiMagicCase(),
	))

	properties.TestingRun(t, gopter.ConsoleReporter(false))
}

// ============================================================================
// test case types
// ============================================================================

// magicDivTestCase represents a single magic divisor pattern to test.
type magicDivTestCase struct {
	// divisor is the original divisor D (in [2, 2^16] for test speed)
	divisor uint64
	// magic is the magic constant M computed from D and shift
	magic uint64
	// shift is the shift amount S
	shift uint8
	// kind is signed or unsigned
	kind MagicDivKind
}

// nonMagicCase represents an IR block that does NOT contain a magic divisor pattern.
type nonMagicCase struct {
	// op is the binary operator used (not mul+shift)
	op ir.BinaryOperator
	// constVal is the right-hand constant
	constVal uint64
}

// multiMagicCase represents a block with multiple magic divisor patterns.
type multiMagicCase struct {
	// cases is the list of individual magic divisor patterns
	cases []*magicDivTestCase
}

